using Auth0.MyOrganizationApi.Core;
using global::System.Text.Json;
using global::System.Text.Json.Serialization;

namespace Auth0.MyOrganizationApi;

[Serializable]
public record GetConfigurationResponseContent : IJsonOnDeserialized
{
    [JsonExtensionData]
    private readonly IDictionary<string, JsonElement> _extensionData =
        new Dictionary<string, JsonElement>();

    /// <summary>
    /// Array of supported strategies
    /// </summary>
    [Optional]
    [JsonPropertyName("allowed_strategies")]
    public IEnumerable<IdpStrategyEnum>? AllowedStrategies { get; set; }

    [Optional]
    [JsonPropertyName("connection_deletion_behavior")]
    public IdpConnectionDeletionBehaviorEnum? ConnectionDeletionBehavior { get; set; }

    [JsonIgnore]
    public ReadOnlyAdditionalProperties AdditionalProperties { get; private set; } = new();

    void IJsonOnDeserialized.OnDeserialized() =>
        AdditionalProperties.CopyFromExtensionData(_extensionData);

    /// <inheritdoc />
    public override string ToString()
    {
        return JsonUtils.Serialize(this);
    }
}
